package com.example.zuji.javaclass;

public class ZuJi {
	int userImage;
	String content;
	String zujiTime;
	boolean isComeMsg;
	public int getUserImage() {
		return userImage;
	}
	public void setUserImage(int userImage) {
		this.userImage = userImage;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getZujiTime() {
		return zujiTime;
	}
	public void setZujiTime(String zujiTime) {
		this.zujiTime = zujiTime;
	}
	public boolean isComeMsg() {
		return isComeMsg;
	}
	public void setComeMsg(boolean isComeMsg) {
		this.isComeMsg = isComeMsg;
	}
}
